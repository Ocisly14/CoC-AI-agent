export const chaseRule = `# Chase Rules (LLM Ready)

## What counts as Chase
- Chase = opposing goals with distance as the core variable; continuous attempts to close or widen the gap.
- Use Chase when: multiple sides are moving; one pursues while another flees; paths/obstacles/distance changes matter; resolution needs multiple beats, not one roll.
- Not Chase: a single sprint into cover (Combat/Stealth), fast movement with no opponent, static standoff or negotiation (Social), melee where you reach target in one round (Combat).
- Quick test: when distance is the central variable and takes repeated actions to resolve, it is a Chase.

## Entering Chase
- Keeper structural call, not a roll. Enter Chase if any: both sides choose movement as main action; someone flees mid-combat before distance is closed; stealth fails and escape begins; NPC/monster starts sustained pursuit.
- Once in Chase: pause normal combat rounds; Chase framing takes over.

## How checks work
- No single fixed roll; choose per situation.
- Normal movement often needs no roll.
- Roll when confronting obstacles or risky moves: vault fences, push through crowds, jump gaps, climb/swim, sharp turns, dangerous driving.
- Failures change the chase state rather than freeze it.

## Outcomes
- Success: open distance, catch target, shake pursuit, gain positional advantage, transition to other modes (Stealth/Combat/Exploration).
- Failure: distance shrinks, forced onto a worse route, stamina loss, stumble/injury, forced into Combat.
- Chase failures are incremental setbacks, not instant defeat.

## Push rules
- Push is allowed but high risk; player must describe a more aggressive route or action.
- Push failure examples: fall and take damage, path blocked, caught immediately, vehicle loses control.

## Time scale
- Chase is scene-level, composed of multiple beats of a few to ~15 seconds; the whole chase fills a scene.
- Ends when: distance hits zero (-> Combat); distance opens enough (-> Stealth/Exploration); a side gives up; environment blocks pursuit.

## Relevant skills
- Core: Dodge (avoid obstacles), Jump, Climb, Swim.
- Movement/control: Stealth to drop pursuit at the end; Track to follow; Drive Auto/Pilot for vehicle chases.
- Situational: Listen (direction when visuals are lost), Spot Hidden (find shortcuts/exits), Throw (create obstacles), Mechanical Repair (sabotage the environment).
- Attributes: DEX for agility, CON for endurance, STR for forcing through, POW for will under panic.
`;
